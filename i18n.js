export function t(key, lang = 'pt') {
  return STRINGS[lang]?.[key] ?? STRINGS.pt[key] ?? key;
}

export function getLangByPais(pais) {
  return pais === 'PY' ? 'es' : 'pt';
}

/** Aplica i18n a todos os elementos com [data-i18n] dentro de `root`. */
export function aplicarI18n(root, lang) {
  root.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key, lang);
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.setAttribute('placeholder', t(key, lang));
  });
}
